	public int solution(String S) {
		
		int decimalValue = Integer.parseInt(S, 2);
		// test
		System.out.println("decimalValue : " + decimalValue);
		
		int rstCount = 0;
		rstCount = subtractOrDivide(decimalValue, rstCount);
		
		return rstCount;
	}

	private int subtractOrDivide(int value, int count) {
		
		if (value == 0) {
			return count;
		}
		
		if (value % 2 > 0) { // odd
			value -= 1;
			count++;
		} else { // even
			value /= 2;
			count++;
		}
		return subtractOrDivide(value, count);
	}
	
